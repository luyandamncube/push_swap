int	ft_isdelimiter(const char *s, char c)
{
	unsigned int	res;

	res = 0;
	while (s && *s)
	{
		while (*s && *s == c)
			s++;
		if (*s && *s != c)
		{
			while (*s && *s != c)
				s++;
			res++;
		}
	}
	return (res);
}
